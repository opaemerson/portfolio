    <p class="dropcap">
    Ganolia sera um Dungeon Crawler com um formato hibrido que combina elementos de boardgame com um aplicativo interativo, 
    oferecendo aos jogadores a experiencia de explorar masmorras em um tabuleiro fisico, com miniaturas e dados, 
    enquanto gerenciam suas cartas virtuais para construir o deck perfeito.
    </p>
    <!--##topico##-->
    <h2>Como funciona apos a compra do jogo?</h2>

    <p>
    O jogo contara com miniaturas representando tanto os personagens jogaveis quanto os temiveis monstros que servirao como desafios para os jogadores. 
    Alem disso, ao adquirir o jogo, os compradores receberao um serial unico e valido que deverao inserir ao baixar o aplicativo. 
    Uma vez validado, eles terao a oportunidade de criar novas partidas, com um limite de ate seis jogadores por sala.
    </p>

    <p>
    Cada jogador, ao ingressar na partida, sera atribuido com um conjunto inicial de cartas, 
    ajustado de acordo com a classe do personagem que tenham escolhido para a aventura. 
    O jogo ira conter um mapa pre-definido, desenhado sob um gradiente, que servira como casas para as miniaturas.
    </p>

    <p>
    O jogo segue uma mecanica ja estabelecida, que, em sua essencia, 
    envolve rolagem de dados para determinar eventos, como o causamento de dano. 
    Quando um jogador causa dano, ele seleciona a carta correspondente no aplicativo e aplica-a ao monstro em questao. 
    </p>

    <p>O jogo tera uma progressao com final, definindo cada partida como uma sessao de Galonia.</p>

    <!--##topico##-->
    <h3>Objetivo do jogo</h3>
    
    <h4>Quest Principal</h4>
    <p>A ideia é criar uma inteligência que monte uma Quest personalizada para cada jogador, que pode combinar diferentes situações, como a fabricação de itens comuns e raros, a derrota de monstros e até mesmo a aniquilação de Chefes de Elite (Semi-Boss).</p>
    
    <h4>Quest Final</h4>
    <p>Após todos os jogadores finalizarem as quests principais, terão a oportunidade de resolver a Quest do End Game. Um final com um Boss específico e aleatório para cada fim de jogo, equilibrando o poder do Boss de acordo com o número de jogadores. Todos os jogadores se juntam para tentar derrotar este Boss Final. </p>
    <p>Ao derrotar o Boss Final, você ganhará um pacote, que oferecerá a oportunidade de adquirir itens para adicionar à sua biblioteca. Se você ou seu grupo conseguirem derrotar o Boss Final, a Quest do End Game será concluída, e o jogo será considerado "100% Concluído". Caso não consigam derrotar o Boss Final, a porcentagem de progresso no jogo será proporcional ao dano causado ao Boss, resultando em porcentagens variando de 90% a 99% de conclusão, indicando que o Boss foi derrotado parcialmente, mas não completamente.</p>
    
    <!--##topico##-->
    <h2>Diferenciais do jogo</h2>
    <ul>
    <li>
    <p>Cada partida é propensa a ser única, utilizando inteligência para quests e garantindo a diversidade de experiência. Além disso, há a possibilidade de encantar itens durante as partidas atuais para uso em partidas futuras. </p>
    </li>

    <li>
    <p>O jogo oferecerá uma venda de pacotes de miniaturas, ao comprar um pacote físico, você enriquecerá sua "biblioteca", Portanto agora, se o jogador decidir adentrar em uma partida o jogo será enriquecido com base nas cartas disponíveis em sua biblioteca, oferecendo oportunidades ao grupo de obtê-las também. </p>
    </li>

    <li>  
    <p>Eventos nativos Dungeons apresentam uma variedade de monstros aleatórios que podem surgir em toda sessão de jogo. </p>
    </li>

    <li>
    <p>O jogo também irá proporcionará eventos exclusivos em um formato online, quando esta dungeon aparecer no mapa, os jogadores terão a oportunidade de se envolver.
    No entanto, esse evento é realizado com um mapa exclusivo e dados virtuais, tudo no ambiente online. </p>
    </li>
    </ul>
    

    <!--##topico##-->
    <h2>Pacotes</h2>

    <p>A ideia é dar início a uma comercialização fora do jogo padrão, desta maneira, busco encontrar uma história tão rica que faça união sobre a maioria dos personagens e monstros, deste modo é possível realizar a comercialização de miniaturas que estão fora do jogo padrão. </p>
    Há dois tipos de pacotes disponíveis:

    <h4>Pacote de Miniatura</h4> 
    Este pacote inclui uma miniatura juntamente com algumas cartas aleatórias de equipamentos ou receitas.
    
    <h4>Pacote no Jogo</h4> 
    Este pacote não contém a miniatura, mas pode ser adquirido por qualquer jogador durante a partida.

    <h2>O que seria a Biblioteca?</h2>

    <p>Este é o local de consulta na qual contém tanto as cartas de monstros quanto as de personagens que foram adquiridos. </p>
    <p>Sua biblioteca funciona como um catálogo de colecionáveis, permitindo que você utilize suas conquistas em qualquer partida que iniciar.</p>
    <p>Uma observação importante sobre <b>Perda de Cartas:</b> Lembre-se de que as cartas adquiridas por pacotes são consideradas permanentes, já as que foram "encantadas" podem ser perdidas em partidas.</p>
    
    <!--##topico##-->
    <h3>Mapas</h3>
    <p>Os mapas sao as peças fisicas que se constroi ao centro da mesa...etc...</p>
    
    <h4>Territorio Central</h4>
    <p>Essa parte do jogo é essencial, pois fornece um formato para posicionar as peças distintas. Geralmente, é nela que ocorre a centralização, reunindo todos os monstros dos territórios diferentes.</p>
    
    <h4>Territorio Distintos</h4>
    <p>Essas são peças que se unem à parte central do mapa. Monstros de territórios distintos não têm a capacidade de invadir uns aos outros, mas podem coexistir no território central.</p>
    
    <h4>Objetos ??</h4>
    <p>Adiconar eventos que introduzem objetos ao mapa, estes podem ser interagidos tanto com os personagens como os monstros.</p>
    
    <!--##topico##-->

    <h3>Moedas</h3>
    <p>Com a usabilidade de combinar as células necessárias para a troca de itens na loja, realizar a ação de compra consumirá uma de suas ações na rodada.</p>
    <p>Aquisição: Drop, Venda ou Conclusão de Qst.</p>

    <h3>Receitas</h3>
    <p>Esta carta possui o poder de invocar um item, no entanto, é essencial fundir os itens mencionados na receita para criá-lo.</p>
    <p>Aquisição: Drop, Compra ou Pacotes</p>

    <h3>Equipamentos</h3>
    <p>Os equipamentos estão divididos em várias categorias de raridade, e essa raridade pode influenciar os atributos do equipamento.</p>
    <p>Os equipamentos têm classificações que correspondem aos seus preços, seguindo uma escala de preços de acordo com a raridade.</p>
    <p>Cada personagem utiliza um item de acordo com sua classe.</p>
    <p>Cada carta especifica quantos dados e que tipo de dado o equipamento utiliza, podendo variar de um D6 a um D12.</p>

    <h3>Materiais</h3>
    <p>A combinação de materiais comuns geralmente resulta na criação de equipamento, que pode ser utilizado para proteção ou ataque.</p>

    <!--##topico##-->
    <h3>Loja Comum</h3>
    <p>São itens que ficam fixados em mesa, contendo cinco cartas disponiveis para compra, podendo ser embaralhada a cada X rodadas e sendo resposta a cada compra efetuada.</p>
    
    <h3>Loja Especial</h3>
    <p>São venda de itens exclusivos relacionados a datas comemorativas e situações especiais podem ser adquiridos sob certas condições.</p>

    <!--##topico##-->
    <h3>Criaturas</h3>
    <p>Nomeados como criaturas, esses são os adversários que os personagens enfrentam durante o jogo. </p>
    <p>As criaturas estão divididas em diferentes categorias de raridade, e essa raridade pode influenciar em seus atributos.</p>
    
    <h4>Recompensas de Criaturas</h4>
    <p>A recompensa é concedida após a aniquilação de um monstro, ou seja, quando o HP do monstro é reduzido a zero. Existem dois tipos de recompensas: a recompensa por dano final e a recompensa por dano maior.</p>
    <p>Isso implica que o jogador que infligir o maior dano ao monstro também receberá a recompensa correspondente. Se o jogador que causou o dano final for o mesmo que causou o dano maior, ele receberá duas recompensas.</p>
    
    <!--##topico##-->
    <h3>Personagens</h3>
    <p>Os personagens desempenham um papel crucial no jogo. Cada personagem é único, com um conjunto exclusivo de habilidades que os diferencia uns dos outros. Essas habilidades únicas podem ser vitais para o sucesso no jogo, pois oferecem vantagens estratégicas específicas.</p>
    <p>Além disso, todos os personagens pertencem a uma classe, que define seu estilo de jogo e papel dentro da equipe. A classe de um personagem influencia suas habilidades e sua função em combate, proporcionando uma diversidade de estratégias e táticas para os jogadores explorarem.</p>

